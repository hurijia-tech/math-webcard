def save_mappings():
    os.makedirs("keyword_mappings", exist_ok=True)

    mappings = {
        "바이블": BIBLE_KEYWORDS,
        "유형해결의법칙": YUHYUNG_KEYWORDS,
        "자이스토리": ZISTORY_KEYWORDS
    }

    for book_name, keywords in mappings.items():
        filename = f"keyword_mappings/{book_name}_keywords.json"
        with open(filename, "w", encoding="utf-8-sig") as f:
            json.dump(keywords, f, ensure_ascii=False, indent=2)
        print(f"✅ 저장 완료: {filename}")

        # 키워드별 문제 수 출력
        for kw, ids in keywords.items():
            print(f"   [{kw}] {len(ids)}개")
        print()

